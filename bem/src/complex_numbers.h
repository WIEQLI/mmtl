#ifndef complex_numbers_h
#define complex_numbers_h

/* definitions required for use of complex numbers */

typedef struct {
  //real part
  float real;         
  //imaginary part           
  float imag;		
} COMPLEX;

typedef struct {
  //real part
  double real;     
  //imaginary part               
  double imag;
} DOUBLE_COMPLEX;

#endif
